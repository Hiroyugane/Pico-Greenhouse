_BLANK_GLYPH = bytes(8)


class FrameBuffer:
    """
    Minimal FrameBuffer shim for CPython host testing.

    Matches the MicroPython framebuf.FrameBuffer API used by ssd1306.py
    and oled_display.py:
        fill, fill_rect, pixel, hline, vline, rect, text, blit
    """

    def __init__(
        self, buffer: bytearray, width: int, height: int, format: int, stride: int = 0
    ):
        self._buf = buffer
        self.width = width
        self.height = height
        self._format = format
        self._stride = stride if stride else width

    def fill(self, color: int) -> None:
        """Fill entire framebuffer with color (0 or 1)."""
        val = 0xFF if color else 0x00
        for i in range(len(self._buf)):
            self._buf[i] = val

    def fill_rect(self, x: int, y: int, w: int, h: int, color: int) -> None:
        """Fill a rectangle with color."""
        for dy in range(h):
            for dx in range(w):
                self.pixel(x + dx, y + dy, color)

    def pixel(self, x: int, y: int, color: int = None) -> int | None:
        """Get or set a pixel. Returns pixel value when color is None."""
        if x < 0 or x >= self.width or y < 0 or y >= self.height:
            return 0
        if self._format == MONO_VLSB:
            byte_idx = (y // 8) * self._stride + x
            bit = y % 8
            if color is None:
                return (self._buf[byte_idx] >> bit) & 1
            if color:
                self._buf[byte_idx] |= 1 << bit
            else:
                self._buf[byte_idx] &= ~(1 << bit)
            return None
        # For other formats just use a flat byte-per-pixel fallback
        idx = y * self._stride + x
        if idx < len(self._buf):
            if color is None:
                return self._buf[idx]
            self._buf[idx] = color & 0xFF
        return None

    def hline(self, x: int, y: int, w: int, color: int) -> None:
        """Draw a horizontal line."""
        for dx in range(w):
            self.pixel(x + dx, y, color)

    def vline(self, x: int, y: int, h: int, color: int) -> None:
        """Draw a vertical line."""
        for dy in range(h):
            self.pixel(x, y + dy, color)

    def rect(self, x: int, y: int, w: int, h: int, color: int) -> None:
        """Draw a rectangle outline."""
        self.hline(x, y, w, color)
        self.hline(x, y + h - 1, w, color)
        self.vline(x, y, h, color)
        self.vline(x + w - 1, y, h, color)

    def text(self, string: str, x: int, y: int, color: int = 1) -> None:
        """Render a string using the 8×8 built-in font (1 pixel per column)."""
        cx = x
        for ch in string:
            code = ord(ch)
            glyph = _FONT.get(code, _BLANK_GLYPH)
            for col, byte in enumerate(glyph):
                for row in range(8):
                    if byte & (1 << row):
                        self.pixel(cx + col, y + row, color)
            cx += 8  # each glyph is 8px wide (5 + 3 spacing)

    def blit(self, fbuf: "FrameBuffer", x: int, y: int, key: int = -1) -> None:
        """Copy another FrameBuffer onto this one at (x, y)."""
        for dy in range(fbuf.height):
            for dx in range(fbuf.width):
                px = fbuf.pixel(dx, dy)
                if key == -1 or px != key:
                    self.pixel(x + dx, y + dy, px)
